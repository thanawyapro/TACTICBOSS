import fs from 'node:fs';
import path from 'node:path';
const root = process.cwd();
const checks = [];
const add = (name, ok, detail) => checks.push({name, ok, detail});
const read = p => fs.readFileSync(path.join(root,p),'utf8');
const runtime = read('public/runtime-config.js');
add('Supabase runtime config populated', !/url:\s*['"]\s*['"]/.test(runtime) && !/anonKey:\s*['"]\s*['"]/.test(runtime), 'Required for production authentication and cloud sync.');
const gradle = read('android/app/build.gradle');
const pkg = JSON.parse(read('package.json'));
add('Android version matches package', gradle.includes(`versionName "${pkg.version}"`), `package=${pkg.version}`);
add('Legal pages present', ['public/privacy.html','public/terms.html','public/delete-account.html'].every(f=>fs.existsSync(path.join(root,f))), 'Privacy, terms and deletion URLs are mandatory launch assets.');
const sensitiveFiles = [runtime, read('.env.example'), read('src/lib/supabaseClient.ts')].join('\n');
add('No service role key value in tracked release files', !/SUPABASE_SERVICE_ROLE_KEY\s*=\s*\S+|service_role\s*[:=]\s*['\"]eyJ/i.test(sensitiveFiles), 'Frontend must contain only the public anon key.');
add('Launch-safe screenshot privacy', !/image_preview:\s*imageDataUrl/.test(read('src/App.tsx')), 'Uploaded screenshot previews must not be persisted by the launch client.');
add('PWA assets present', ['public/manifest.json','public/service-worker.js','public/offline.html','public/assets/icon-192.png','public/assets/icon-512.png'].every(f=>fs.existsSync(path.join(root,f))), 'Required for installable web release.');
console.log('\nTactic Boss AI launch readiness\n');
for (const c of checks) console.log(`${c.ok?'PASS':'BLOCK'}  ${c.name} — ${c.detail}`);
const blocked = checks.filter(c=>!c.ok);
console.log(`\n${checks.length-blocked.length}/${checks.length} launch gates passed.`);
if (blocked.length) process.exitCode=2;
