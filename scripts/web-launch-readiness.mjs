import fs from 'node:fs';
import path from 'node:path';
const root = process.cwd();
const checks = [];
const add = (name, ok, detail) => checks.push({name, ok, detail});
const read = p => fs.readFileSync(path.join(root,p),'utf8');
const exists = p => fs.existsSync(path.join(root,p));
const runtime = read('public/runtime-config.js');
const app = read('src/App.tsx');
add('Supabase production runtime config populated', !/url:\s*['"]\s*['"]/.test(runtime) && !/anonKey:\s*['"]\s*['"]/.test(runtime), 'Required for web authentication and cloud sync.');
add('No private Supabase key in frontend', !/service_role\s*[:=]\s*['"]eyJ|SUPABASE_SERVICE_ROLE_KEY\s*=\s*\S+/.test([runtime, read('.env.example'), read('src/lib/supabaseClient.ts')].join('\n')), 'Only the public anon key may be shipped.');
add('Legal and account deletion pages present', ['public/privacy.html','public/terms.html','public/delete-account.html'].every(exists), 'Required for a public web service.');
add('PWA and offline assets present', ['public/manifest.json','public/service-worker.js','public/offline.html','public/assets/icon-192.png','public/assets/icon-512.png'].every(exists), 'Required for installable web release.');
add('No screenshot preview persistence', !/image_preview:\s*imageDataUrl/.test(app), 'User screenshot previews must stay on-device.');
add('Paid checkout is not falsely enabled', !/\$6\.99|\$12\.99/.test(app) && /current release is free/.test(app), 'The web launch must not sell plans before secure billing exists.');
add('Netlify SPA routing configured', exists('netlify.toml') && exists('public/_redirects'), 'Required for refresh-safe client routes.');
add('Release version marked web production final', /web-production-final/.test(read('public/version.json')), 'Confirms this is the web-only final release line.');
console.log('\nTactic Boss AI web launch readiness\n');
for (const c of checks) console.log(`${c.ok?'PASS':'BLOCK'}  ${c.name} — ${c.detail}`);
const blocked=checks.filter(c=>!c.ok);
console.log(`\n${checks.length-blocked.length}/${checks.length} web launch gates passed.`);
if(blocked.length) process.exitCode=2;
